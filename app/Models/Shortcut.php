<?php

namespace App\Models;

use Illuminate\Database\Eloquent\Builder;
use Illuminate\Database\Eloquent\Factories\HasFactory;
use Illuminate\Database\Eloquent\Model;

/**
 * App\Models\Shortcut
 *
 * @method static Builder|Shortcut newModelQuery()
 * @method static Builder|Shortcut newQuery()
 * @method static Builder|Shortcut query()
 * @mixin \Eloquent
 */
class Shortcut extends Model
{
    protected $table = 'shortcut';
    protected $fillable = ['type', 'content', 'sort_num'];
    protected $hidden = ['type'];

    public $timestamps = false;

    public static function boot()
    {
        parent::boot(); // TODO: Change the autogenerated stub
        static::addGlobalScope('sort', function (Builder $builder) {
            return $builder->orderBy('sort_num')->orderBy('id');
        });
    }
}
